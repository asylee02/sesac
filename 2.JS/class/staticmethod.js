class MathOperation{
  static add(x,y){
    return x+y;
  }

  static sub(x,y){
    return x-y
  }
  
  static mul(x,y){
    return x*y
  }

  static div(x,y){
    return x/y
  }
}


console.log(MathOperation.add(2,3))
console.log(MathOperation.sub(2,3))
console.log(MathOperation.mul(2,3))
console.log(MathOperation.div(2,3))